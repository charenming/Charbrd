package chaboard;

public class Chaboard {

	
	private int chaID;
	private String chaTitle;
	private String userID;
	private String chaDate;
	private String chaContent;
	private int chaAvilable;
	
	public int getChaID() {
		return chaID;
	}
	public void setChaID(int chaID) {
		this.chaID = chaID;
	}
	public String getChaTitle() {
		return chaTitle;
	}
	public void setChaTitle(String chaTitle) {
		this.chaTitle = chaTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getChaDate() {
		return chaDate;
	}
	public void setChaDate(String chaDate) {
		this.chaDate = chaDate;
	}
	public String getChaContent() {
		return chaContent;
	}
	public void setChaContent(String chaContent) {
		this.chaContent = chaContent;
	}
	public int getChaAvilable() {
		return chaAvilable;
	}
	public void setChaAvilable(int chaAvilable) {
		this.chaAvilable = chaAvilable;
	}
}
